var arr = [1, 2, 3, 4];

// Push Method 

arr.push(10); // arr:[1, 2, 3, 4, 10]
console.log(arr)

// pop method 

arr.pop() // arr:[1, 2, 3, 4]
console.log(arr)

// shift method

arr.shift() // arr:[2, 3, 4]
console.log(arr)

// unshift method

arr.unshift(-1) // arr:[-1, 2, 3, 4]
console.log(arr)

